#include "libftprintf.h"

void	ft_putunbr(unsigned int n)
{
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	if (n < 10)
	{
		ft_putchar(n + 48);
	}
}
